    ABSTRACT(TokenSubtype.KEYWORD, TokenSubtype.MODIFIER),
    VARARG(TokenSubtype.KEYWORD, TokenSubtype.MODIFIER),
    OVERRIDE(TokenSubtype.KEYWORD, TokenSubtype.MODIFIER),
    PRIVATE(TokenSubtype.KEYWORD, TokenSubtype.MODIFIER),
    STRUCT(TokenSubtype.KEYWORD),
    PROTECTED(TokenSubtype.KEYWORD, TokenSubtype.MODIFIER),
    PUBLIC(TokenSubtype.KEYWORD, TokenSubtype.MODIFIER),
    STATIC(TokenSubtype.KEYWORD, TokenSubtype.MODIFIER),
    OBJECT(TokenSubtype.KEYWORD),
    THIS(TokenSubtype.KEYWORD),
    FUNC(TokenSubtype.KEYWORD),
    IF(TokenSubtype.STATEMENT, TokenSubtype.KEYWORD, TokenSubtype.FLOW_CONTROL),
    ELSE(TokenSubtype.STATEMENT, TokenSubtype.KEYWORD, TokenSubtype.FLOW_CONTROL),
    TRUE(TokenSubtype.CONSTABLE),
    FALSE(TokenSubtype.CONSTABLE),
    TRY(TokenSubtype.STATEMENT, TokenSubtype.KEYWORD, TokenSubtype.FLOW_CONTROL),
    CATCH(TokenSubtype.STATEMENT, TokenSubtype.KEYWORD, TokenSubtype.FLOW_CONTROL),
    FINALLY(TokenSubtype.STATEMENT, TokenSubtype.KEYWORD, TokenSubtype.FLOW_CONTROL),
    WHEN(TokenSubtype.STATEMENT, TokenSubtype.KEYWORD, TokenSubtype.FLOW_CONTROL),
    WHERE(TokenSubtype.KEYWORD),
    WHILE(TokenSubtype.STATEMENT, TokenSubtype.KEYWORD),
    DO(TokenSubtype.STATEMENT, TokenSubtype.KEYWORD),
    FOR(TokenSubtype.STATEMENT, TokenSubtype.KEYWORD),
    RETURN(TokenSubtype.FLOW_CONTROL, TokenSubtype.KEYWORD),
    BREAK(TokenSubtype.FLOW_CONTROL, TokenSubtype.KEYWORD),
    CONTINUE(TokenSubtype.FLOW_CONTROL, TokenSubtype.KEYWORD),
    NEWLINE(TokenSubtype.WHITESPACE),
    GENERIC(TokenSubtype.OTHER),
    NONE(TokenSubtype.NONE),
    ANY(TokenSubtype.OTHER);
    val isOperator = subtypes.contains(TokenSubtype.OPERATOR)

    enum class TokenSubtype {
        STATEMENT,
        FLOW_CONTROL,
        CONSTABLE,
        GROUP_START,
        GROUP_END,
        OPERATOR,
        LEFT_OPERATOR,
        RIGHT_OPERATOR,
        LEFT_OR_RIGHT_OPERATOR,
        BINARY_OPERATOR,
        BINARY_OR_LEFT_OPERATOR,
        WHITESPACE,
        KEYWORD,
        MODIFIER,
        OTHER,
        ASSIGNMENT,
        NONE
    }


}